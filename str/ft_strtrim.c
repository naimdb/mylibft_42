#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char			*res;
	size_t			len;

	if (s1 == 0 || set == 0)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len - 1]))
		len--;
	res = ft_substr((char *)s1, 0, len);
	return (res);
}
